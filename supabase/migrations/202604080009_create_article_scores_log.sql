create table if not exists public.article_scores_log (
  id bigint generated by default as identity primary key,
  pipeline_run_id bigint not null references public.pipeline_runs(id) on delete cascade,
  article_id bigint references public.articles(id) on delete set null,
  team_id bigint not null references public.teams(id) on delete restrict,
  source_id bigint not null references public.sources(id) on delete restrict,
  source_name text not null,
  source_type text not null,
  fetch_date date not null,
  headline text not null,
  original_url text not null,
  word_count integer,
  relevance_score integer,
  significance_score integer,
  credibility_score integer,
  uniqueness_score integer,
  composite_score integer,
  selection_reasoning text,
  rejection_reason text,
  passed_quality_gate boolean not null default false,
  passed_threshold boolean not null default false,
  threshold_at_time integer,
  summary_generated boolean not null default false
);
