create table if not exists public.articles (
  id bigint generated by default as identity primary key,
  source_id bigint not null references public.sources(id) on delete cascade,
  team_id bigint not null references public.teams(id) on delete restrict,
  title text not null,
  original_url text not null unique,
  raw_content text,
  ai_summary text,
  published_at timestamptz not null,
  category text check (category in ('transaction', 'injury', 'game_analysis', 'rumor', 'general')),
  relevance_score integer,
  significance_score integer,
  credibility_score integer,
  uniqueness_score integer,
  composite_score integer,
  selection_reasoning text,
  rejection_reason text,
  validation_notes text,
  passed_threshold boolean not null default false,
  summary_version integer not null default 1,
  previously_published boolean not null default false,
  word_count integer,
  is_mid_game boolean not null default false,
  stat_source_article_id bigint references public.articles(id) on delete set null
);
